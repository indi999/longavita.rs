	    Bicom alergo test nam omogućuje da precizno i tačno odredimo glavni alergen a osnovni i prvi korak lečenja 
	    se sastoji u potpunom izbacivanju iz ishrane tog alergena na period od minimum dva do tri meseca koliko traju i terapije.
        <br /><w>.</w></blockquote>  

	    Nakon izbacivanja iz ishrane glavnog alergena, već nakon četiri do pet dana uočava se jasan prekid progresije 
	    bolesti. Tada, ukoliko pacijent hranom unese glavni alergen, odmah će primetiti novo javljanje promena na koži.
	    Zbog toga je veoma važno pridržavati se uputstva za ishranu koje dobijate od našeg lekara. Uspeh lečenja zavisi 
	    od dobre saradnje pacijenta i lekara-terapeuta.<br /><br />  

        Iskustva su pokazala da imuni sistem osoba sa neurodermatitisom često ima neko od opterećenja kao što su geopatogeno 
        zračenje, elektrosmog, opterećenja od vakcina, teških metala, hemijskih sredstava, parazita i drugih supstanci i
        mikroorganizama.<br /><br />  
        
        <blockquote><br />
        Bicom aparatom se identifikuju opterećenja na imunom sistemu i pristupa se njihovom eliminisanju. 
