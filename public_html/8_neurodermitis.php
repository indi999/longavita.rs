     <h5> 
     	Neurodermatitis ili atopijski dermatitis je hronična upalna bolest koja se 
     	najčešće javlja kod osoba sklonih alergijama. Važnu ulogu u nastanku bolesti 
     	ima naslednost, jer od neurodermatitisa najčešće oboljevaju osobe u čijoj porodici
     	već ima obolelih od neke alergijske bolesti. Prvi znakovi neurodermatitisa se
     	mogu javiti u bilo kojoj životnoj dobi, javlja se kod odojčadi i dece a podjednako
     	često se javlja kod muškaraca kao i kod žena.<br /><br /> 
        
      <i class="podnaslov">Simptomi neurodermatitisa</i><br />
	    Prvi i najčešći simptomi neurodermatitisa su suvi ljuspasti osip na koži i svrab. Kod odojčadi se
	    najčešće javlja na licu, vratu i temenu dok su kod male dece i odraslih kožne promene najčešće na 
	    licu, podkolenicama, nadlakticama, te pregibu laktova i kolena. Suva i osetljiva koža s vremenom gubi
	    sposobnost da veže vodu i dodatno se isušuje, a posledica je upala kože i jak svrab. Svrab uzrokuje
	    češanje i pucanje kože, javljaju se otvorene rane sa mehurićima iz kojih curi bistra tekućina. 
	    Iritirana koža pogodna je za javljanje bakterija i gljivica što može izazvati infekciju.<br /><br />

	    <i class="podnaslov"> Uzroci javljanja neurodermatitisa</i><br />
	    Premda je sklonost ka neurodermatitisu genetski uslovljena i nasledna, promene na koži mogu uzrokovati 
	    različiti faktori. Budući da je neurodermatitis alergijska bolest, najčešći alergeni koji ga uzrokuju su
	    kravlje mleko, pšenica, gluten, belance jaja, polen, kućna prašina, grinje, životinjska dlaka, perje te
	    konzervansi i aditivi u hrani. Za razliku od odraslih osoba kod kojih neurodermatitis najčešće zavisi o
	    stresu, kod male dece i odojčadi najčešći uzrok neurodermatitisa je alergija na hranu.<br /><br /> 

	    <i class="podnaslov">Iskustva iz naše prakse </i><br />
	    Pšenica pripada grupi <i class="podnaslov">hroničnih alergena</i> koji najčešće izazivaju neurodermatitis i hronični ekcem. 
	    Akutni oblici alergije na pšenicu se javljaju retko.<br /><br />

	    Kod pacijenata sa neurodermatitisom gotovo uvek postoji alergija na hranu i to na jednu od najčešćih 
	    životnih namirnica, najčešće kravlje mleko ili pšenica. Kako se bolest razvija, osobe sa neurodermatitisom 
	    razvijaju akutne oblike alergija na polen, kućnu prašinu, citrusno voće, orašaste plodove i drugo.<br /><br /> 
        
