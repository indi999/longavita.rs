        <i class="podnaslov">Koliko terapija je potrebno?</i><br />
        Na terapije se dolazi jednom nedeljno a individualno je koliki broj terapija je potreban. U proseku kod blažih oblika neurodermatitisa
        potrebno je 7 do 10 terapija a kod težih oblika i do 15. Terapije se doživljavaju kao veoma opuštajuće, bezbedne su, bezbolne i konforne
        i traju od 30-60 minuta. Bicom aparatom, bez ikakvih štetnih posledica, mogu se tretirati mala deca, adolescenti i osobe svih životnih 
