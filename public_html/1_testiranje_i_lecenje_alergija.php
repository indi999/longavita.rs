	   <h5>
	   <i class="podnaslov">Kontaktna alergija</i><br />
	   Kontaktna alergija se ubraja u posebnu kategoriju alergijskih reakcija. Iskustva Bicom lekara i terapeuta pokazala su da kontaktna 
	   alergija podleže sopstvenim pravilima. <br /><br />

       Suštinska razlika između ovog oblika alergije i ostalih je osnovni imunološki mehanizam koji se odvija jedino na ćelijskom nivou i
       nema dokaza da antitela kruže kroz krvotok.<br /><br />

       Klasična manifestacija ćelijske hiperosetljivosti  je alergijski kontaktni ekcem. Najčešći okidači su metali (kao što su bakar, hrom,
       kobalt), gumena pomoćna sredstva, kozmetika, sredstva za dezinfekciju, konzervansi i lekovi u medicinskoj pripremi za eksternu upotrebu
       kao i supstance povrća, u svakom slučaju kada je  supstanca u direktnom kontaktu sa kožom. <br /><br />

       <i class="podnaslov">Pseudoalergija</i><br />
       Pseudoalergijska reakcija se uglavnom ne javlja sve dok se ne dostigne određena količina supstance koja pokreće reakciju u organizmu. 
       Često se posebno odnosi na hemijske dodatke hrani koji se - pošto su dozvoljeni zakonom -  često dodaju hrani u prekomernim količinama.<br /><br /> 
       
       <i class="podnaslov">Od odlučujuće važnosti za pojavu pseudoalergijskih reakcija su:</i><br />
       <ol class="disc"><h5>
	    	<li>Pacijentova individualna osetljivost </li>
	    	<li>Ukupna količina supstance, koja se često obezbeđuje u isto vreme različitom hranom i različitim hemijskim sastavima (na primer azo boje, 
	    		benzoati, fosfati, parabeni itd.)</li></h5>
       </ol><br /><br />

       Prelasci u alergiju u pravom smislu, su veoma brzi, pošto veliki broj aditiva može da pokrene pravu alergijsku reakciju.<br /><br /> 

       Bicom biorezonantna  terapija  pogodna je i efikasna u slučaju lečenja pseuodalergija.<br /><br />

