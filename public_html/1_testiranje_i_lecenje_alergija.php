    <h5>
    <i class="podnaslov">Akutni oblici alergije</i><br />
    Akutne alergije su alergijske reakcije na supstance prilikom kojih dolazi do 
    senzibilizacije a koje samo privremeno ili povremeno dolaze u kontak sa organizmom, 
    u svakom slučaju ne neprekidno (inhalatorni alergeni, životinjska dlaka, kućna prašina,
    buđ, hemikalije, mirisi i slično).<br /><br />
    </h5>
    <h5>
    <i class="podnaslov">Simptomi akutnih alergija:</i><br /></h5>
    <ul class="no-bullet"><h5>
		<li><u style="color:#54b953">KOŽA:</u> Akutni alergijski osip, koprivnjača, Angioedem (Kvinkeov edem).</li>
		<li><u style="color:#54b953">OČI i NOS:</u> Sezonski i višegodišnji alergijski konjuktivitis i rinitis (alergijski rinitis)</li>
		<li><u style="color:#54b953">BRONHIJE:</u> Spastički bronhitis, akutna alergijska bronhijalna astma</li>
		<li><u style="color:#54b953">CREVA:</u> Akutni alergijski gastritis, enteritis, colitis.</li></h5>
    </ul>
    
    <h5>
    <i class="podnaslov">Hronični (centralni) oblici alergije</i><br />
    Javljaju se kod ljudi koji generalno imaju jaku naslednu predispoziciju, često u ranom detinjstvu, gde dolazi do senzibilizacije
    na osnovne životne namirnice koje se većinom svakodnevno unose u organizam (mleko, pšenica) ili na supstancu koja je neprestano 
    prisutna u organizmu (kanida kod hronične crevne mikoze).<br />
    Čestim ili stalnim kontaktom sa alergenom simptomi su gotovo uvek maskirani, što znači da neposredan međuodnos između simptoma i 
    alergena uglavnom nije prepoznatljiv.
    <br /><br />

    Ovo nisu akutne reakcije na određene alergene, već alergije na supstance koje se unose hranom svakodnevno ili sa kojima je telo u 
    stalnom kontaktu. Glavna, maskirana alergija je stalno, teško opterećenje i konstantni stres za imuni sistem. Težina oboljenja zavisi
    od toga koliko dugo organizam uspeva da kompenzira stalni stres. U bilo kom momentu će kompezacioni mehanizam biti preopterećen i 
    srušiće se i to pre svega kada se na postojeća dodaju neka druga, nova telesna ili duševna opterećenja<br /><br />

    Tako je glavna, maskirana alergija često baza za razvijanje sledećih akutnih formi alergija. I upavo kod multialergičara (ljudi koji 
    imaju brojne alergije na različite supstance) iskustvo je pokazalo, da se u osnovi akutnih alergija nalazi jedna glavna alergija na 
    jednu osnovnu životnu namirnicu. Maskirane alergije takođe igraju glavnu ulogu i kod mnogih drugih hroničnih bolesti, kao na primer 
    kod neurodermatitisa, reumatskih oboljenja, ulceroznih kolitisa itd.<br /><br /> 
    
    <blockquote><br /> Za uspešnu terapiju multialergičara važno je otkloniti glavne alergene. Pojedine akutne alergije nestanu same od 
    sebe nakon otklanjanja glavnih opterećenja, druge se većinom, nakon kratkotrajnih terapeutskih postupaka koji posle uslede, lako 
    otklanjaju pomoću BICOM aparata.<br /><w>.</w>
    </blockquote>

