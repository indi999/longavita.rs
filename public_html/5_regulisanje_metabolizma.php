    <i style="color:#54b953">Metabolizam</i> ili izmena, menjanje, promena, je skup svih hemijskih reakcija u telu, odnosno ćelijama organizma. Njegov 
    intenzitet merimo količinom toplote oslobođene tokom tih reakcija. Sve što bi moglo uticati da se hemijske reakcije povećaju,
