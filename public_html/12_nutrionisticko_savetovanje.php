        <h5>
         <i class="podnaslov"> Masti</i><br />
         Masti su komplesni molekuli, sačinjeni od masnih kiselina i glicerola. Telo ih treba za rast, energiju kao i za mnoge druge
         funkcije, jer pre svega, masti su jedna od osnovih komponenti svake žive ćelije. Telo koristi masti za sintezu hormona kao i
         ostalih supstanci potrebnih za ljudske aktivnosti. Masti su najsporiji izvor energije, ali energetski najefikasnija forma 
         hrane. Jedan gram masti sadrži 9 Kcal, što je za raliku od ugljenih hidrata i proteina koji sadrže 4 Kcal u 1 gramu, više 
         nego duplo veća količina energije. Pošto su masti efikasan izvor energije, svaki višak će upravo biti skladišten kao mast. 
         I to najviše u delovima abdomena kao i ispod kože. Međutim, telo je svojstveno i da skladišti višak masti u krvnim sudovima.
         Masti delimo na monozasićene, poli zasićene i zasićene, na masne kiseline – Omega 3 i Omega 6, kao i na trans masti koje 
         nastaju hidrogenizacijom i prženjem. Trans masti su loše masti jer su to oštećene masti u toku termičke obrade ostalih masti.
         Masti životinjskog porekla su većinom zasićene, dok su biljnog poli i monozasićene (izuzetak su kokosovo i palmino ulje koje
         su zasićene). Esencijalne masne kiseline i nezasićene masti su od neizmernog značaja, kao i zasićene masti i one nisu loše 
         kako se opšteprihvaćeno misli, to je veliki mit i zabluda. One osim što daju energiju organizmu one su neophodne za normalno
         funkcionisanje organizma i svih živih ćelija. Esencijalne masne kiseline povoljno deluju na oči, kožu, srce, snižavanje LDL-a.
         <br /><br />

         <i class="podnaslov">Pravoremeni unos i kombinovanje hrane</i><br />
         Mnogo je bitnije kada jesti određenu hranu, nego koja hrana se jede, uslovno rečeno. Svaki tip hrane, da li su to ugljeni 
         hidrati, masti ili proteini, ima drugačiji uticaj na kompletno trenutno stanje organizma, na osećaj sitosti i gladi, raspoloženje,
         energetske varijacije, pad i porast šećera (glukoze) u krvi itd. Kada se probudimo, telu je potrebna energija, jer je ono tokom 
         noći bilo u “hibernaciji” bez unosa hrane. Ujutru je vreme za ugljene hidrate, za one koji imaju veliku želju za prostim šećerima,
         kao i za unos vlakana. Ta energija će obezbediti kvalitetan dan i ona će se lako potrošiti. Svakako, složeni ugljeni hidrati treba 
         da preovlađuju pre nego prosti. Za one koji nemaju preveliku želju obrok sa mastima i proteinima je idealan.<br /><br />
