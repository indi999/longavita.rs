	     upravo loše iskombinovana i nekvalitetna hrana. Što se užina tiče, za njih je idelano jesti voće, koštunjavo voće poput orasa, lešnika, badema,
	     ili u određenim slučajevima nešto “konkretnije” kao što je kvalitetan integralni sendvič sa tunom.<br /><br />

		 <i class="podnaslov">Tečnost</i><br />
		 Vodu piti ujutru nakon ustajanja. Nakon treninga (šetnje, trčanja). U toku dana. Pre svakog obroka. Pre užine. Dosta vode. Voda pospešuje sve
		 vitalne funkcije u organizmu. Manjak vode osim što vodi organizam u propast, utiče i na indirektno povećanje telesne težine. Tako da voda direktno
		 i indirektno može da pomogne u svrhu mršavljenja. Zeleni čaj, osim na stotine povoljnih dejstava koje ima na organizam, itekako može pomoći u 
		 cilju mršavljenja. Ne sadrži velike količine kofeina tako da ne utiče na zavaravanja centralnog nervnog sistema o trenutnom stanju zamorenosti.
		 Zbog visokog sadržaja katehina deljuje detoksikativno na organizam, pospešujući izbacivanje štetnih materija iz organizma, kao i vode (diuretik), 
		 što će dalje usloviti povećanoj potrošnju tačnije unošenju iste, što i jeste cilj. Zeleni čaj dokazano deluje pozitivno na sprečavanje pojavljivanja raka.
		 Što više tečnosti u toku dana treba da uđe i izađe iz organizma. Početni period je period čišćenja, koji će se bazirati na povećanom unosu čajeva. 
		 Idealna kombinacija je zeleni čaj ujutru, ili u toku pre podneva i još minimum jedan do dva biljna čaja u toku dana, posle podne ili predveče. Biljni 
		 čaj takođe deluje detoksikativno, opuštajuće i smirujuće, a ne sadrži kofein, stoga je idealan i za kasnije sate. Takođe, uz dosta povrća i voća se unosi 
		 dodatna tečnost.<br /><br />


		 <i class="podnaslov">Ubrzavanje metabolizma</i><br />
		 Osnovni problem kod većine ljudi koji se bore sa viškom kilograma je loš metabolizam, koji je upravo produkt gladovanja. Kada je ishrana bazirana na malo 
		 dnevnih obroka, velika je pauza između istih, i metabolizam se vremenom usporava jer je to prirodni odbranbeni mehanizam organizma, kada mu malo dajete, on 
		 malo i troši. Stoga veliki akcenat treba da bude na ubrzavanju metabolizma, a to se postiže sa pet ili šest dnevnih obroka i užina. Kada se metabolizam vremenom 
		 ubrza uz promenu načina ishrane i fiziškom aktivnošću, telo će tokom dana trošiti mnogo veće zalihe kalorija, što je upravo i cilj. Osim doručka, ručka i večere,
		 sasvim je normalno, ili bolje reći obavezno imati i međuobroke tj. užine. Između doručka i ručka, pa i dve između ručka i večere, kao i jednu manju nakon večere.
		 Redovnim unosom hrane se ubrzava metabolizam, čime prirodno dolazi i do veće potrošnje energije. Obroci ne treba da budu obilni, što se sa navedenim načinom 
		 definitvno postiže.<br /><br />
		 Najgora usluga koju možemo da učinimo organizmu je preobilan obrok sa velikom pauzom između njega i prethodnog ili sledećeg jer će tada doći do osicalicije 
		 energije, nadutosti, lošeg osećaja, prispavaće nam se, metabolizam će se vremenom usporiti. a i organizam ni približno neće moći da iskoristi bitne nutritijenti 
		 koji su uneseni u tom obroku. Fizička aktivnost dodatno ubrzava metabolizam.<br /><br />

		 <i class="podnaslov">Fizička aktivnost</i><br />
		 Fizička aktivnost je obavezna u procesu mršavljenja, ali ne iscrpljujuća fizička aktivnost, već umerena i efikasna. Velika je greška što se istoj pristupa sa 
		 pogrešne strane, i mnogi preteruju u početku, i brzo odustanu. Fizičku aktivnost treba shvatiti kao “ventil” za opuštanje od svakodnevnog života, obaveza, posla.
		 Vežbanje sa ciljem sagorevanja kalorija je pogrešno vežbanje.<br /><br />

		 Treba stvoriti potrebu da se od 2 do 4 puta nedeljno upražnjava neki vid fizičke aktivnosti. U početku je bitno bar 2 do 3 puta ići na trčanje, najefikasnije ujutru.
		 Ukoliko trčanje nije moguće treba kombinovati trčanje i šetnju dok se ne uđe u “formu”; 20 do 30 minuta je sasvim dovoljno i lagani tempo je sasvim prihvatljiv.
		 Odlična stvar može biti sprint s vremena na vremena jer će trening sa par sprinteva potstaći sagorevanje kalorija nakon treninga, pošto je “manji šok” za organizam. 
		 Trening snage (teretana) je drugi veoma efikasan vid treninga. Trening snage u velikoj meri ubrzava metabolizam na duže staze, sličan efekat kao i kod 
		 sprinteva – povećava se potrošnja kalorija i u periodu nakon samog treninga. Trening snage stimuliše mnoge procese koje će pospešiti sagorevanje masti kao i lučenje 
		 i pravilan rad hormona. Treningom snage se povećava mišićna masa, a upravo povećanje mišićne mase i istovremeno smanjenje masti ima direktan uticaj na ubrzavanje
		 metabolizma. Sumirano, u okviru jedne nedelje, idealno bi bilo bar dva puta odraditi laganu aerobnu aktivnost poput trčanja ili šetnje i po mogućnosti ubaciti jedan
		 trening snage. Nakon samo par nedelja, za one koji su prethodno bili neaktivni uz ova 3-4 treninga u toku nedelje se već oseća pozitivan efekat.<br /><br />

