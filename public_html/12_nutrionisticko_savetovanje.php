         <h5>
         U toku dana se treba manje osloniti na ugljene hidrate, a više na kombinaciju masti i proteina. Uveče akcenat treba staviti na proteine
         i zdrave masti koji će nahraniti mišiće nakon dana, i zasititi naš organizam. Zdrave masti će usporiti delovanje proteina pa će i duže
         tokom noći amino kiseline dospevati do mišića, čime se skraćuje period kada je organizam pasivan i kada se metabolizam potpuno usporava.
         Takođe jako važna stavka u pravilnoj ishrani predstavlja kombinovanje hrane. Različiti tipovi hrane se različito kombinuju, i različitim 
         kombinovanjem se dobija totalno drugačiji odraz namirnica i nutritijenata na organizam. Proteini se kombinuju sa zdravim mastima, jer kao
         što je i prethodno napisano, osim što će usporiti absorbavanje unetih proteina, one će obezbediti energiju koja će vrlo efikasno moći da
         se iskoriti i narednog dana. Ugljene hidrate međutim ne treba previše kombinovati sa mastima, jer su ugljeni hidrati znak da svaki višak
         masti ide u salo, kao i da svaki višak ugljenih hidrata ide u masti. Najgora moguća kombinacija prestavlja prost šećer plus trans masti,
         jer će to dovesti do velikih oscilacija u osećaju gladi i oscilacija u prilivu energije jer su trans masti loše. U tom slučaju je uneta
         velika količina kalorija, a osećaj sitosti će vrlo brzo nestati i sav višak kalorija će preći u masti a zatim u salo. Ugljeni hidrati 
         se ujutru mogu kombinovati sa zdravijim mastima u normalnim količinama, i za ručak sa proteinima, međutim treba obratiti pažnju na 
         ograničavanje pri kombinaciji velikih količina ugljenih  hidrata i proteina zbog nadutosti i varenja. Svaki obrok treba da obiluje pretežno
         jednom ili do dve vrste nutritijenata, jer će oni tada imati i najefikasniji uticaj na organizam.<br />
	     Prilikom pravilne ishrane i pravilnog kombinovanja u kratkom vremenskom periodu se oseti neverovatna razlika u osećaju nakon obroka, u 
