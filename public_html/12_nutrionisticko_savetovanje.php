         <h5><i class="podnaslov">Proteini</i><br />
         Sačinjeni su od amino kiselina, odnosno predstavljaju kompleksne lance različitih dužina i različitih kombinacija 
         amino kiselina. Kao i kod kompleksnih ugljenih hidrata, telu je potrebno duže vreme za razlaganje, stoga proteini
         obezbeđuju energiju telu jedan duži vremenski period, svakako duži nego ugljeni hidrati. Postoji 20 amino kiselina,
